import path from 'node:path';
import fs from 'node:fs';
import { pathToFileURL, fileURLToPath } from 'node:url';
import type { Plugin, ViteDevServer } from 'vite';
import { compileString as sassCompileString, Logger } from 'sass-embedded';

type ThemeStyleEntry = {
    filepath: string;
    extensions: string[];
    resolveMapping: Record<string, string> | string[];
    assetName: string | null;
};

type ThemeFilesJson = {
    script: unknown[];
    style: ThemeStyleEntry[];
    themeId: string;
    technicalName: string;
    domainUrl: string;
};

/**
 * Reads and parses var/theme-files.json.
 * Returns null when the file is absent or malformed so the caller can warn once.
 */
function readThemeFiles(themeFilesPath: string): ThemeFilesJson | null {
    if (!fs.existsSync(themeFilesPath)) return null;

    try {
        return JSON.parse(fs.readFileSync(themeFilesPath, 'utf-8')) as ThemeFilesJson;
    } catch {
        return null;
    }
}

/**
 * Returns the absolute path to the dumped theme-variables SCSS file for a
 * given themeId, or null when neither the per-theme nor the generic fallback
 * file exists.
 *
 * The PHP ThemeCompiler writes:
 *   var/theme-variables/{themeId}.scss  ← per-theme, preferred
 *   var/theme-variables.scss            ← generic fallback
 */
function resolveThemeVariablesFile(projectRoot: string, themeId: string): string | null {
    const perTheme = path.join(projectRoot, 'var/theme-variables', `${themeId}.scss`);
    if (fs.existsSync(perTheme)) return perTheme;

    const fallback = path.join(projectRoot, 'var/theme-variables.scss');
    if (fs.existsSync(fallback)) return fallback;

    return null;
}

/**
 * Compiles all SCSS entries from theme-files.json into a single CSS string.
 *
 * Rather than compiling each stylesheet in isolation, a virtual SCSS document is
 * generated that:
 *
 *   1. @imports the dumped theme-variables SCSS file written by
 *      `bin/console theme:compile` (var/theme-variables/{themeId}.scss or the
 *      generic fallback). This file contains all admin-configured theme
 *      variable overrides (colours, fonts, custom plugin variables, …).
 *      Without it, theme-specific variables are undefined when SCSS entries
 *      that rely on them are compiled.
 *
 *   2. Declares the asset-path variables that PHP's theme compiler and the
 *      Webpack sass-loader normally inject (e.g. $app-css-relative-asset-path).
 *
 *   3. @imports every entry from theme-files.json in document order so that
 *      variables defined in early entries (e.g. base.scss) are in scope when
 *      later entries (e.g. skin/_base.scss) are processed.
 *
 * All tilde-prefixed aliases from each entry's resolveMapping are collected
 * into a shared set of Dart Sass FileImporters so imports like
 * `~vendor/bootstrap/…` resolve correctly regardless of which entry they
 * appear in.
 */
function compileAll(entries: ThemeStyleEntry[], themeId: string, domainUrl: string, projectRoot: string): { css: string; loadedFiles: string[] } {
    // Collect all resolve-mapping aliases across all entries into one set.
    const aliasMap = new Map<string, string>();
    for (const entry of entries) {
        if (!Array.isArray(entry.resolveMapping) && typeof entry.resolveMapping === 'object') {
            for (const [alias, realPath] of Object.entries(entry.resolveMapping)) {
                aliasMap.set(alias, realPath);
            }
        }
    }

    const importers = [
        // Resolve tilde-prefixed aliases, e.g. ~vendor/bootstrap/…
        ...Array.from(aliasMap.entries()).map(([alias, realPath]) => ({
            findFileUrl(url: string): URL | null {
                const prefix = `~${alias}/`;
                if (!url.startsWith(prefix)) return null;
                return pathToFileURL(path.join(realPath, url.slice(prefix.length)));
            },
        })),
        // compileString has no implicit filesystem resolver, so absolute paths
        // written into our virtual @import statements must be handled explicitly.
        {
            findFileUrl(url: string): URL | null {
                if (path.isAbsolute(url)) return pathToFileURL(url);
                return null;
            },
        },
    ];

    // 1. Theme-variables file: contains admin-configured overrides
    // generated by bin/console theme:compile.
    const themeVariablesFile = resolveThemeVariablesFile(projectRoot, themeId);
    const themeVariablesImport = themeVariablesFile
        ? `@import '${themeVariablesFile.split(path.sep).join('/')}';`
        : '';

    // 2. Asset-path variable preamble.
    //    Uses !default so values from the theme-variables file take precedence.
    const assetPath = `${domainUrl}/theme/${themeId}/assets`;
    const assetOverrides = [
        `$app-css-relative-asset-path: '${assetPath}' !default;`,
        '$sw-asset-theme-url: \'\' !default;',
        '$sw-asset-asset-url: \'\' !default;',
        '$sw-asset-public-url: \'\' !default;',
        '$sw-asset-sitemap-url: \'\' !default;',
        '$sw-features: () !default;',
        `$theme-id: '${themeId}' !default;`,
    ].join('\n');

    // 3. @import each entry with its absolute path (forward-slashes for Sass compatibility).
    const imports = entries
        .map(e => `@import '${e.filepath.split(path.sep).join('/')}';`)
        .join('\n');

    const result = sassCompileString(`${themeVariablesImport}\n${assetOverrides}\n${imports}`, {
        importers,
        quietDeps: true,
        logger: Logger.silent,
    });

    // Collect every file Sass touched so callers can watch transitive dependencies.
    const loadedFiles = result.loadedUrls
        .filter(u => u.protocol === 'file:')
        .map(u => fileURLToPath(u));

    return { css: result.css, loadedFiles };
}

/**
 * Vite plugin — Storefront SCSS compiler for the component dev server.
 *
 * In production the PHP theme compiler (bin/console theme:compile) is solely
 * responsible for compiling SCSS to CSS. During `npm run dev:server` this
 * plugin takes over so developers see SCSS changes immediately without having
 * to re-run the PHP compiler:
 *
 * - Reads var/theme-files.json (produced by bin/console theme:dump) to
 *   discover every SCSS entry registered with the active theme, including
 *   entries from apps and plugins.
 * - Compiles all entries together as a single virtual SCSS document and
 *   serves the result as raw CSS at /theme-scss/all.css.
 *   Variable declarations from earlier entries are
 *   therefore in scope for later ones, avoiding "Undefined variable" errors
 *   that occur when skin partials are compiled in isolation.
 * - The CSS URL is registered in storefront_components.dev.json by
 *   dev-import-map-plugin — a single flag file covers both JS and CSS.
 * - Watches all registered SCSS files; a change triggers a full page reload so
 *   the browser fetches the freshly compiled CSS from the middleware.
 * - Watches var/theme-files.json itself so that re-running theme:dump (e.g.
 *   after installing a new plugin) picks up new entries without a dev server
 *   restart.
 */
export function themeScssWatcherPlugin(projectRoot: string): Plugin {
    const themeFilesPath = path.join(projectRoot, 'var/theme-files.json');

    let styleEntries: ThemeStyleEntry[] = [];
    let themeId = '';
    let domainUrl = '';
    let themeVariablesFile: string | null = null;
    // All files involved in the last compilation (entries + every transitive import).
    const watchedScssFiles = new Set<string>();

    function applyThemeFiles(themeFiles: ThemeFilesJson): void {
        styleEntries = themeFiles.style;
        themeId = themeFiles.themeId;
        domainUrl = themeFiles.domainUrl;
        themeVariablesFile = resolveThemeVariablesFile(projectRoot, themeFiles.themeId);
    }

    function registerLoadedFiles(server: ViteDevServer, loadedFiles: string[]): void {
        for (const f of loadedFiles) {
            if (!watchedScssFiles.has(f)) {
                watchedScssFiles.add(f);
                server.watcher.add(f);
            }
        }
    }

    return {
        name: 'sw-theme-scss',
        apply: 'serve',

        configureServer(server: ViteDevServer) {
            const { logger } = server.config;

            // ----------------------------------------------------------------
            // Middleware — compile all entries as one virtual SCSS document and
            // serve the resulting CSS at /theme-scss/all.css on every request.
            // Re-compiling on each request means edits are always fresh.
            // ----------------------------------------------------------------
            server.middlewares.use((req, res, next) => {
                if (req.url?.split('?')[0] !== '/theme-scss/all.css') return next();

                if (styleEntries.length === 0) {
                    res.setHeader('Content-Type', 'text/css; charset=utf-8');
                    res.end('/* [sw-theme-scss] No SCSS entries found in theme-files.json */');
                    return;
                }

                try {
                    const { css, loadedFiles } = compileAll(styleEntries, themeId, domainUrl, projectRoot);
                    registerLoadedFiles(server, loadedFiles);
                    res.setHeader('Content-Type', 'text/css; charset=utf-8');
                    res.setHeader('Cache-Control', 'no-cache');
                    res.end(css);
                } catch (err) {
                    const message = err instanceof Error ? err.message : String(err);
                    logger.error(`[sw-theme-scss] Compile error: ${message}`, { timestamp: true });
                    res.setHeader('Content-Type', 'text/css; charset=utf-8');
                    res.statusCode = 500;
                    res.end(`/* [sw-theme-scss] Compilation error:\n   ${message}\n*/`);
                }
            });

            // ----------------------------------------------------------------
            // Initial setup
            // ----------------------------------------------------------------
            const initial = readThemeFiles(themeFilesPath);

            if (!initial) {
                logger.warn(
                    '[sw-theme-scss] var/theme-files.json not found. ' +
                    'Run bin/console theme:dump to enable Vite SCSS compilation.',
                    { timestamp: true },
                );
                return;
            }

            applyThemeFiles(initial);

            server.watcher.add(themeFilesPath);
            // Pre-seed with entry files and the theme-variables file so the change
            // handler works immediately, before the warm-up compilation completes.
            styleEntries.forEach(e => {
                watchedScssFiles.add(e.filepath);
                server.watcher.add(e.filepath);
            });
            if (themeVariablesFile) {
                watchedScssFiles.add(themeVariablesFile);
                server.watcher.add(themeVariablesFile);
            }

            server.httpServer?.once('listening', () => {
                logger.info(
                    `[sw-theme-scss] Serving ${styleEntries.length} SCSS entry(s) as /theme-scss/all.css`,
                    { timestamp: true },
                );
                if (!themeVariablesFile) {
                    logger.warn(
                        `[sw-theme-scss] No theme-variables file found for theme ${themeId}. ` +
                        'Run bin/console theme:compile to generate it.',
                        { timestamp: true },
                    );
                }

                // Warm-up compile: discover and watch all transitive SCSS imports
                // so that changes to partials trigger a reload from the first edit,
                // not only after the browser has loaded /theme-scss/all.css once.
                try {
                    const { loadedFiles } = compileAll(styleEntries, themeId, domainUrl, projectRoot);
                    registerLoadedFiles(server, loadedFiles);
                    logger.info(
                        `[sw-theme-scss] Watching ${watchedScssFiles.size} SCSS file(s) (including transitive imports).`,
                        { timestamp: true },
                    );
                } catch {
                    // Errors are already surfaced by the middleware on first request.
                }
            });

            // ----------------------------------------------------------------
            // File-change handling
            // ----------------------------------------------------------------
            server.watcher.on('change', (changedFile) => {
                if (changedFile === themeFilesPath) {
                    const updated = readThemeFiles(themeFilesPath);
                    if (!updated) return;

                    applyThemeFiles(updated);
                    // Re-seed entries and re-run warm-up for the new theme.
                    watchedScssFiles.clear();
                    styleEntries.forEach(e => {
                        watchedScssFiles.add(e.filepath);
                        server.watcher.add(e.filepath);
                    });
                    if (themeVariablesFile) {
                        watchedScssFiles.add(themeVariablesFile);
                        server.watcher.add(themeVariablesFile);
                    }
                    try {
                        const { loadedFiles } = compileAll(styleEntries, themeId, domainUrl, projectRoot);
                        registerLoadedFiles(server, loadedFiles);
                    } catch { /* surfaced by middleware */ }
                    logger.info('[sw-theme-scss] theme-files.json reloaded — entry list updated.', { timestamp: true });
                    server.ws.send({ type: 'full-reload' });
                    return;
                }

                const affected = watchedScssFiles.has(changedFile);
                if (!affected) return;

                logger.info(
                    `[sw-theme-scss] SCSS changed: ${path.relative(projectRoot, changedFile)} → full reload`,
                    { timestamp: true },
                );

                server.ws.send({ type: 'full-reload' });
            });
        },
    };
}
